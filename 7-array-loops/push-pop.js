//push() e pop() - inserindo e removendo itens ao fim de um array

const nomesDeEstudantes = ['jose', 'joao', 'maria', 'joana', 'andre'];

//Metodo push ou comando push

nomesDeEstudantes.push('pedro');
//coloca pedro no final do array

//Metodo pop ou comando pop
nomesDeEstudantes.pop();
//pop retira o ultimo item do array