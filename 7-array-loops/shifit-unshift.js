// shift() e unshift() - inserindo e removendo itens ao começo de um array
//remover elementos do array
const nomesDeEstudantes = ['jose', 'joao', 'maria', 'joana', 'andre'];

//remover o primeoro elemento do array
nomesDeEstudantes.shift();
console.log(nomesDeEstudantes);
//incerir um elemento no começo do array
nomesDeEstudantes.unshift('jose');
console.log(nomesDeEstudantes);