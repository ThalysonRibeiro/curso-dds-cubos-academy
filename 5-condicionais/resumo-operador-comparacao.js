//Maior que: > Retorna verdadeiro caso o operador da esquerda seja maior que o da direita.
const maiorQue = 5 > 3;

// Maior ou igual: >= Retorna verdadeiro caso o operador da esquerda seja maior ou igual o da direita.
const maiorOuIgual = 5 >= 5;

//Menor que: < Retorna verdadeiro caso o operador da esquerda seja menor que o da direita.
const menorQue = 4 < 6;

//Menor ou igual: <= Retorna verdadeiro caso o operador da esquerda seja menor ou igual o da direita.
const menorOuIgual = 4 <= 6;

//igual: == Retorna verdadeiro caso s operadores sejam igual (idependente do tipo). 
let igual = 3 == "3";
igual = 2 == 2;

//Diferente: != Retorna verdadeiro caso os operadores não sejam iguais. 
let diferente = 3 != 4;
diferente = 4 != "3";

//Estritamente igual: === Retorna verdadeiro caso os operadores sejam igual e do mesmo tipo.
let estritamenteIgual = 3 === 3;
estritamenteIgual = "a" === "a";

//Estritamente diferente: !== Retorna verdadeiro caso os operadores não sejam iguais e/ou não sejam do mesmo tipo.
let estritamentediferente = 3 !== "3";
