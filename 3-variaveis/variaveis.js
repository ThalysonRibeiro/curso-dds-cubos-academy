//criando variavel const
// const é uma variavel constante. é uma variavel que nãp pode mais ser mudada.
//não se pode fazer uma reatribuição a variavel const.
{
    const primeiroNome = "Thalyson";
console.log(primeiroNome);
}

{
//fazer uma reatribuição a mesma variavel.
//variavel let pode reatribuir outro valor a mesma variavel. ex:
let primeiroNome = "Thalyson";
primeiroNome = "jhonsonn";
console.log(primeiroNome);
}